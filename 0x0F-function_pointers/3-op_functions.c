/**
 * op_add - this adds integer together
 * @a: the first number
 * @b: the second number
 * daniel oluwanimotele
 *
 * Return: the sum of numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - this subtract different integer
 * @a: first number
 * @b: second number used
 * mayowa daniel
 *
 * Return: the subtracted numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mod - this function takes the modulus of two numbers
 * @a: the first number used
 * @b: the second number used
 * written  by daniel
 *
 * Return: the answer of the mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - this function divides two numbers
 * @a: the first number
 * @b: the second number
 *
 * written by daniel
 * Return: the divided number as answer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - this funtion multiples two different numbers
 * @a: the first number
 * @b: The second number
 * written by daniel
 * Return: the multiplied number as answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
